#!/usr/bin/env node
// Generates the admin-Retro font-size override block from backend.css +
// admin.css. VT323 renders ~30% smaller visually than Inter at the same px,
// so every admin text class gets a ~1.2x px bump to match Galaxy density.
// The output is a plain CSS block printed to stdout — paste it into
// src/styles/themes.css between the labelled Admin Retro markers.
//
// Run from repo root:
//   node scripts/generate-retro-admin-sizes.cjs
//
// Why a generator? backend.css has ~120 distinct font-size rules in
// absolute px. Writing Retro overrides by hand means every new admin
// text class risks being missed; a generator keeps the bump proportional
// across every class automatically.

const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const files = [
  path.join(root, 'src/backend.css'),
  path.join(root, 'src/admin.css'),
];

// Target px sizes. VT323 at <Galaxy px> reads about the density of Inter
// at <Retro px> — empirically tuned. The 15→18 anchor is the
// `.be-checkbox-row` example the user confirmed visually.
const scaleMap = {
  10: 12, 11: 13, 12: 14, 13: 16, 14: 17, 15: 18, 16: 19, 17: 20,
  18: 22, 20: 24, 22: 26, 24: 29, 26: 31,
};

const selectorToPx = new Map();
for (const file of files) {
  let text = fs.readFileSync(file, 'utf8');
  text = text.replace(/\/\*[\s\S]*?\*\//g, '');
  const re = /([^{}]+)\{([^{}]*?)\}/g;
  let m;
  while ((m = re.exec(text)) !== null) {
    const rawSelector = m[1].trim().replace(/\s+/g, ' ');
    const body = m[2];
    const match = body.match(/font-size\s*:\s*(\d+(?:\.\d+)?)px/);
    if (!match) continue;
    if (rawSelector.startsWith('@') || rawSelector.startsWith('from') || rawSelector.startsWith('to') || /^\d/.test(rawSelector)) continue;
    const selectors = rawSelector.split(',').map(s => s.trim()).filter(Boolean);
    const px = parseFloat(match[1]);
    for (const sel of selectors) {
      if (sel.includes('[data-theme=')) continue;
      if (/^(:root|html|body)(\s|$|[.:#])/.test(sel)) continue;
      const existing = selectorToPx.get(sel);
      // If the same selector appears with multiple sizes across files/rules,
      // keep the largest one — it tends to be the "base" size and the other
      // is a contextual shrink that we don't need to redeclare.
      if (existing === undefined || existing < px) selectorToPx.set(sel, px);
    }
  }
}

const bySize = {};
for (const [sel, px] of selectorToPx) {
  if (!bySize[px]) bySize[px] = new Set();
  let scoped;
  if (sel === '.admin-shell') {
    scoped = '.admin-shell[data-theme="retro"]';
  } else if (sel.startsWith('.admin-shell ')) {
    scoped = sel.replace('.admin-shell ', '.admin-shell[data-theme="retro"] ');
  } else {
    scoped = `.admin-shell[data-theme="retro"] ${sel}`;
  }
  bySize[px].add(scoped);
}

const header = [
  '/* Admin Retro — font-size bumps ~1.2x so VT323 reads at Galaxy density.',
  '   AUTO-GENERATED by scripts/generate-retro-admin-sizes.cjs from',
  '   backend.css + admin.css font-size rules. Regenerate whenever admin',
  '   font-sizes change so the proportional bump stays in sync.',
  '   All selectors are scoped under .admin-shell[data-theme="retro"] —',
  '   the admin isolation boundary; no rule here can leak into a nested',
  '   [data-theme] preview panel. */',
].join('\n');
process.stdout.write(header + '\n');
for (const size of Object.keys(bySize).sort((a, b) => Number(a) - Number(b))) {
  const scaled = scaleMap[size] || Math.round(Number(size) * 1.2);
  const uniq = [...bySize[size]].sort();
  process.stdout.write(`${uniq.join(',\n')} {\n  font-size: ${scaled}px;\n}\n\n`);
}
